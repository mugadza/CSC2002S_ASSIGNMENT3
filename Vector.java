public class Vector{
    float x;
    float y;

    public Vector(float x, float y){
        this.x = x;
        this.y = y;
    }

    public float x(){
        return x;
    }

    public float y(){
        return y;
    }

    public void setX(float x){
        this.x = x;
    }

    public void setY(float y){
        this.y = y;
    }
}